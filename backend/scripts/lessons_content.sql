# Redis — in-memory key-value хранилище

Redis хранит данные в оперативной памяти, что обеспечивает очень высокую скорость доступа — миллионы операций в секунду.

## Основные особенности

- **Скорость**: операции чтения и записи работают за O(1) для большинства структур.
- **Персистентность**: несмотря на работу в памяти, Redis умеет сохранять снапшоты на диск (RDB) и писать лог операций (AOF).
- **Репликация**: встроенная master-replica репликация и Sentinel для автоматического переключения.
- **Кластеризация**: Redis Cluster для горизонтального шардирования.

## Типичные применения

- Кэширование результатов запросов к БД и внешним API.
- Хранение пользовательских сессий веб-приложений.
- Очереди задач и сообщений (через `LIST` или Redis Streams).
- Счётчики и метрики реального времени.
- Распределённые блокировки (паттерн Redlock).
- Rate limiting API.
- Pub/Sub для уведомлений между сервисами.

## Структуры данных

Redis поддерживает не только простые строки, но и богатый набор структур:

- **STRING** — обычные строки, числа, бинарные данные до 512 МБ.
- **LIST** — двусторонние очереди (push/pop с обоих концов).
- **SET** — множества уникальных значений, поддержка объединений и пересечений.
- **HASH** — ассоциативные массивы (поля и значения внутри одного ключа).
- **ZSET** (sorted set) — упорядоченные множества, идеально для топ-листов и рейтингов.
- **STREAM** — append-only лог событий с потребителями и группами.

## Примеры команд

```
SET user:1001:name "Alice"         # строка
GET user:1001:name

LPUSH queue:tasks "task_1"         # в список
RPOP queue:tasks                   # из списка

SADD online:users "alice" "bob"    # множество
SISMEMBER online:users "alice"

HSET cart:42 product_1 3           # хэш
HGETALL cart:42

ZADD leaderboard 500 "alice"       # отсортированное множество
ZREVRANGE leaderboard 0 9          # топ-10
```

