# Архитектура Cassandra

Apache Cassandra — распределённая колоночная СУБД, изначально разработанная в Facebook для обслуживания системы сообщений с миллионами пользователей.

## Кольцевая архитектура

Cassandra использует **peer-to-peer** архитектуру: все узлы кластера равноправны, нет ведущего и ведомого. Данные распределяются по узлам с помощью **консистентного хэширования**: ключ записи хэшируется, и по значению хэша определяется узел, ответственный за её хранение.

Все узлы объединены в логическое «кольцо» — отсюда и название.

## Ключевые свойства

- **Линейное горизонтальное масштабирование**: добавление нового узла увеличивает пропускную способность пропорционально.
- **Tunable consistency**: уровень согласованности задаётся для каждого запроса — от `ONE` (быстро, но возможны неконсистентные чтения) до `ALL` (медленно, но строго согласованно).
- **Отсутствие единой точки отказа**: любой узел можно выключить без потери работоспособности.
- **Оптимизация под запись**: благодаря LSM-tree структуре (Log-Structured Merge Tree) запись выполняется очень быстро — сначала в memtable, потом пакетно на диск.

## Модель данных

В отличие от MongoDB, Cassandra имеет чёткую схему таблиц, но:

- **Нет JOIN** — данные денормализуются и дублируются.
- **Запрос всегда должен включать ключ партиционирования** — иначе придётся опрашивать все узлы кластера.
- **Вторичные индексы ограничены** — они работают, но с оговорками.

## Язык запросов CQL

Cassandra Query Language внешне напоминает SQL:

```sql
CREATE TABLE users_by_country (
  country text,
  user_id uuid,
  name    text,
  email   text,
  PRIMARY KEY (country, user_id)
);

SELECT * FROM users_by_country WHERE country = 'RU';
```

Ключевой момент: `PRIMARY KEY (country, user_id)` означает, что `country` — это partition key (по нему данные распределяются между узлами), а `user_id` — clustering column (по нему данные упорядочиваются внутри одной партиции).

## Применение

Cassandra показывает себя отлично на следующих задачах:

- хранение временных рядов и метрик (миллиарды точек),
- логи событий и аудит,
- социальные ленты и истории действий,
- IoT-данные с высокой нагрузкой на запись,
- каталоги сообщений в мессенджерах.
