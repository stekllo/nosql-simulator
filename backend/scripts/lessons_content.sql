# Графовая модель данных

В Neo4j данные представлены как **граф свойств** (labeled property graph): узлы (вершины) с метками и рёбра (отношения) между ними. И узлы, и рёбра могут иметь произвольный набор свойств.

## Элементы графа

- **Узел (Node)** — сущность (человек, товар, город, событие). Имеет одну или несколько меток (`:Person`, `:Product`) и произвольный набор свойств.
- **Отношение (Relationship)** — связь между двумя узлами. У отношения всегда есть тип (`KNOWS`, `BOUGHT`, `LIVES_IN`) и направление.
- **Свойства** — пары ключ-значение на узлах и отношениях.

## Пример на языке Cypher

```cypher
CREATE (alice:Person {name: 'Alice', age: 30})
CREATE (bob:Person   {name: 'Bob',   age: 25})
CREATE (alice)-[:KNOWS {since: 2020}]->(bob)
```

Создались два узла с меткой `Person` и одно направленное отношение `KNOWS` между ними с атрибутом `since`.

## Почему это полезно

Главное преимущество графовых БД — **высокая производительность при обходе связей**. Запрос «найти друзей друзей Alice, которые живут в Москве» в реляционной СУБД потребует нескольких дорогих JOIN, а в Neo4j записывается одной строкой:

```cypher
MATCH (alice:Person {name: 'Alice'})
      -[:KNOWS]->(:Person)-[:KNOWS]->(foaf:Person)
      -[:LIVES_IN]->(:City {name: 'Москва'})
RETURN DISTINCT foaf.name
```

Время такого запроса почти не зависит от общего размера БД — только от количества рёбер вокруг начальной точки.

## Когда использовать

- **Социальные сети** — друзья, подписки, взаимодействия.
- **Рекомендательные системы** — «похожие товары», «что покупали вместе».
- **Анализ зависимостей** — архитектура программного обеспечения, построение планов развёртывания.
- **Антифрод и кибербезопасность** — анализ цепочек транзакций и подозрительных связей.
- **Графы знаний** — семантические сети, онтологии.
- **Идентификация и контроль доступа** — иерархии прав, группы пользователей.

## Когда НЕ использовать

Графовые БД плохо подходят для:

- плоских табличных данных без связей (тут лучше PostgreSQL),
- массовой аналитики агрегатов (лучше колоночные СУБД — ClickHouse, Cassandra),
- простого кэша (Redis быстрее).
